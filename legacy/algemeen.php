<h2><b>Tips & informatie</h2></b><p>
  

Wacht niet te lang met het geven van de eerste trimbeurt. Een pup kan al naar de trimsalon voor een eerste puppybeurt.
Dit hoeft niet lang in beslag te nemen en zo kan hij vast wennen.<p>

  

De trimsalon is bedoeld voor elke hond! Dus ook kortharige honden kunnen hier terecht.</br>
Een goede wasbeurt en ontwollen helpt ze sneller door de rui. <p>

  

Een hond gaat gemiddeld 4 keer per jaar naar de trimsalon. Dit is afhankelijk van de vachtsoort. <p>

  

Houdt uw hond altijd vrij van parasieten.<p> 

  

Houdt uw hond klit vrij!  Dit veraangenaamd zijn verblijf in de trimsalon en scheelt in prijs.</br>
Borstel en kam hem enkele malen per week. Let op dat u kamt tot op de huid en niet alleen de bovenste laag van de vacht.<p> 

  

Zorg voor een goede voeding, dit komt de vacht en huid van de hond ten goede. <p>

